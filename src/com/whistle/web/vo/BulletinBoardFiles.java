package com.whistle.web.vo;

public class BulletinBoardFiles {

	String code; 
	String name; 
	String type; 
	String bulletinBoardCode;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBulletinBoardCode() {
		return bulletinBoardCode;
	}
	public void setBulletinBoardCode(String bulletinBoardCode) {
		this.bulletinBoardCode = bulletinBoardCode;
	}
	
	
	
}
