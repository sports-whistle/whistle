package com.whistle.web.vo;

public class TicketInfo {

	
	private String teamId;
	private String ticketPriceUrl;
	private String ticketSeatsUrl;
	
	
	
	public String getTeamId() {
		return teamId;
	}
	public void setTeamId(String teamId) {
		this.teamId = teamId;
	}
	public String getTicketPriceUrl() {
		return ticketPriceUrl;
	}
	public void setTicketPriceUrl(String ticketPriceUrl) {
		this.ticketPriceUrl = ticketPriceUrl;
	}
	public String getTicketSeatsUrl() {
		return ticketSeatsUrl;
	}
	public void setTicketSeatsUrl(String ticketSeatsUrl) {
		this.ticketSeatsUrl = ticketSeatsUrl;
	}
	
	
	
	
}
